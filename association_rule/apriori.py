import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

dataset = pd.read_csv('Market_Basket_Optimisation.csv',header=None)
# header=None یعنی دیتای ما هدر نداره
transactions = []
# این لیست قراره کل خریدها رو نگه داره
# هر عضو این لیست = یک سبد خرید
# هر سبد خرید = لیستی از آیتم‌ها
for i in range(0,7501):
    transactions.append([str(dataset.values[i , j]) for j in range(0,20)])
    # i = ردیف ها, j = ستون ها
    
from apyori import apriori
rules = apriori(transactions= transactions,min_support = 0.003,min_confidence = 0.2 , min_lift = 3, min_length = 2, max_length = 2)
# حداقل Support
# یعنی این قانون باید حداقل در 0.3٪ کل خریدها دیده شده باشه
# حداقل Confidence
# یعنی اگر ایکس خرید شد حداقل بیست درصد از مواقع ایگرگ هم خریده شده باشه
# min_length=2,
# max_length=2
# یعنی فقط قوانین دوتایی می‌خوایم
# X → Y
results = list(rules)
# برای اینکه بتونیم روش کار کنیم، به list تبدیلش می‌کنیم

def inspect(results):
    lhs = [tuple(result[2][0][0])[0] for result in results]
    # lhs = Left Hand Side
    # یعنی سمت چپ قانون (اگر این خرید شد...)
    rhs = [tuple(result[2][0][1])[0] for result in results]
    # یعنی سمت راست قانون (...این هم خریده میشه)
    supports = [result[1] for result in results]
    confidence = [result[2][0][2] for result in results]
    lifts = [result[2][0][3] for result in results]
    return list(zip(lhs, rhs, supports, confidence, lifts))
resultsinDataFrame = pd.DataFrame(inspect(results), columns = ['Left-hand Side', 'Right-hand Side', 'Support', 'Confidence', 'Lift'])
resultsinDataFrame.nlargest(n=10 , columns='Lift')




# ============================================================
# توضیح کامل مفهوم Lift در الگوریتم Apriori (با مثال عددی)
# ============================================================

# ما در Apriori سه معیار مهم داریم:
# 1- Support
# 2- Confidence
# 3- Lift   ← مهم‌ترین و در عین حال گیج‌کننده‌ترین

# ------------------------------------------------------------
# فرض کن یک سوپرمارکت داریم با 100 تا خرید (Transaction)
# ------------------------------------------------------------

# تعداد کل خریدها = 100

# 30 نفر نان خریدن
# 20 نفر کره خریدن
# 15 نفر هم نان و هم کره رو باهم خریدن

# ------------------------------------------------------------
# 1️⃣ Support چیست؟
# ------------------------------------------------------------
# Support یعنی:
# "این الگو در چند درصد از کل خریدها دیده شده؟"

# Support(نان → کره) =
# تعداد خریدهایی که نان و کره باهم بودن / کل خریدها

# support = 15 / 100 = 0.15
# یعنی 15٪ کل خریدها شامل نان و کره باهم بوده

# ------------------------------------------------------------
# 2️⃣ Confidence چیست؟
# ------------------------------------------------------------
# Confidence یعنی:
# "اگر X خرید شد، احتمال خرید Y چقدره؟"

# Confidence(نان → کره) =
# تعداد خریدهای نان+کره / تعداد خریدهای نان

# confidence = 15 / 30 = 0.5
# یعنی:
# از هر کسی که نان خریده، 50٪ مواقع کره هم خریده

# تا اینجا همه چیز منطقیه ✔
# ولی هنوز نمی‌دونیم این رابطه واقعیه یا شانسی

# ------------------------------------------------------------
# 3️⃣ Lift چیست؟ (اصل ماجرا)
# ------------------------------------------------------------

# سؤال اصلی Lift اینه:
# آیا خرید کره بعد از نان، واقعاً به خاطر نانه؟
# یا کره کلاً چیز محبوبیه و همه می‌خرنش؟

# Lift بررسی می‌کنه که:
# "این Confidence چقدر از حالت تصادفی قوی‌تره؟"

# ------------------------------------------------------------
# فرمول Lift:
# ------------------------------------------------------------
# Lift(X → Y) = Confidence(X → Y) / Support(Y)

# ------------------------------------------------------------
# محاسبه Lift در مثال ما:
# ------------------------------------------------------------

# Support(کره) =
# 20 / 100 = 0.2

# Confidence(نان → کره) =
# 0.5

# Lift = 0.5 / 0.2 = 2.5

# ------------------------------------------------------------
# تفسیر Lift = 2.5 یعنی چی؟
# ------------------------------------------------------------

# یعنی:
# کسی که نان می‌خره،
# 2.5 برابر بیشتر از حالت عادی کره می‌خره

# این یعنی رابطه نان و کره تصادفی نیست
# و واقعاً به هم وابسته‌ان

# ------------------------------------------------------------
# تفسیر کلی مقادیر Lift:
# ------------------------------------------------------------

# Lift = 1
# یعنی:
# هیچ رابطه‌ای وجود نداره (کاملاً شانسی)

# Lift > 1
# یعنی:
# رابطه مثبت وجود داره
# خرید X باعث افزایش احتمال خرید Y شده

# Lift < 1
# یعنی:
# رابطه منفی وجود داره
# خرید X احتمال خرید Y رو کم کرده

# ------------------------------------------------------------
# چرا Confidence به تنهایی کافی نیست؟
# ------------------------------------------------------------

# فرض کن 90٪ مردم همیشه آب معدنی می‌خرن
# حالا اگر کسی چیپس بخره، احتمالاً آب هم می‌خره

# Confidence(چیپس → آب) خیلی بالا میشه
# ولی این به خاطر چیپس نیست
# به خاطر اینه که آب کلاً خیلی پرمصرفه

# در این حالت:
# Lift ≈ 1
# یعنی رابطه واقعی وجود نداره

